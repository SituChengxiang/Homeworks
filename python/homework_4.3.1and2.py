"""请使用列表推导式实现"""

#设a是一个整数列表，写列表推导式，得到或实现：
a = [random.randrange(20) for i in range(20)]
print(a)
#1. a中的奇数的平方所构成列表
b = [x**2 for x in a if x % 2 == 1]
print(b)
#2. a中的奇数的下标所构成列表
b = [idx for idx, val in enumerate(a) if val % 2 == 1]
print(b)
#3. 判断a中数是否都是偶数
b = all(x % 2 == 0 for x in a)
print(b)
#4. 判断a中的数是否至少有一个是偶数
b = any(x % 2 == 0 for x in a)
print(b)

