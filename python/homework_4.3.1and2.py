import random
#=================================作业题1========================================
"""使用random库中的choice函数生成包含从'abcd'中选取的10个随机字符的列表a。请使用for循环实现。"""
a = []
#在下面写自己的程序
for i in range(10):
    a.append(random.choice('abcd'))
#在上面写自己的程序
print(a)

#=================================作业题2========================================
"""使用循环与index方法将上面生成列表a中’a’的下标存到列表b。请使用for循环实现。"""

b = []
#在下面写自己的程序
start = 0
for _ in range(a.count('a')):
    idx = a.index('a', start)
    b.append(idx)
    start = idx + 1
#在上面写自己的程序
print(b)


